<script lang="ts">
	import { goto } from '$app/navigation';
	import { SearchIcon, ShuffleIcon } from '@lucide/svelte';
	import Content from '../../components/content.svelte';

	let searchInput = $state('');

	const topics = [
		'Quantum Computing',
		'Neural Networks',
		'Black Holes',
		'DNA Replication',
		'Game Theory',
		'Cryptography',
		'Chaos Theory',
		'Fractals',
		'Stoicism',
		'Dark Matter',
		'Neuroplasticity',
		'Time Dilation',
		'Entropy',
		'Symbiosis'
	];

	function explore(topic: string) {
		goto(`/explore/${encodeURIComponent(topic)}`);
	}

	function exploreRandom() {
		explore(topics[Math.floor(Math.random() * topics.length)]);
	}

	function handleSubmit(e: Event) {
		e.preventDefault();
		if (searchInput.trim()) explore(searchInput.trim());
	}
</script>

